IO_Joy1		EQU	0FCH		; Joystick 1 input port
IO_Joy2		EQU	0FFH		; Joystick 2 input port

JOYSTICK:
    OUT	($C0),A
    NOP
    NOP
    NOP
    NOP
    LD	A,B
    OR	A
    JR	NZ,JOYSTICK1
JOYSTICK0:
    IN	A,($FC)
    CPL
    AND $7F
    JP JOYSTICKA
JOYSTICK1:
    IN	A,($FF)
    CPL
    AND $7F
    JP JOYSTICKA

JOYSTICKA:
    RET
