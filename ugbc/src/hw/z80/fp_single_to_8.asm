; z80float is a floating point library targeting the Z80 processor. While this
; project is tested on a TI-84+, the code should be generic enough to run on any
; Z80 CPU with very little modification (mostly defining scrap RAM locations).
;
; adapted by Marco Spedaletti

FPSINGLETO8S:
	PUSH HL
	PUSH DE
	PUSH BC
	LD A, (HL)
	INC HL
	OR (HL)
	LD D, A
	INC HL
	LD A, (HL)
	RLCA
	SCF
	RRA
	LD C,A
	INC HL
	LD A, (HL)
	ADC A,A

	RR E

	CALL FPSINGLETOI8_GET_INT

FPSINGLETOI8_RETURN:
	POP BC
	POP DE
	POP HL
	RET

FPSINGLETOI8_ZERO_ISH:
	XOR A
	RL E
	RET NC
	LD A, C
	ADD A, A
	OR D
	ADD A, 255
	SBC A, A
	RET

FPSINGLETOI8_INFNAN:
	LD A, C
	ADD A, A
	OR D
	SUB 1
	SBC A, A
	RET Z
FPSINGLETOI8_INF:
	LD A, 127
	RL E
	ADC A, 0
	RET

FPSINGLETOI8_GET_INT:
	INC A
	JR Z, FPSINGLETOI8_INFNAN
	JP P, FPSINGLETOI8_ZERO_ISH
	SUB 128
	CP 7
	JR NC, FPSINGLETOI8_INF

	LD H, 0
	LD L, C		; UPPER 8 BITS OF THE SIGNIFICAND, H IS 0
	LD B, A
	INC B
	ADD HL, HL
	DJNZ $-1
	LD A, H
	RL E
	RET NC
	LD A, L
	OR D
	ADD A, 255
	SBC A, A
	SUB H
	LD H, A
	RET

FPSINGLETOU8:

	PUSH HL
	PUSH DE
	PUSH BC
	LD E, (HL)
	INC HL
	LD D, (HL)
	INC HL
	LD A, (HL)
	RLCA
	SCF
	RRA
	LD C, A
	INC HL
	LD A, (HL)
	ADC A, A
	CCF
	JR NC, FPSINGLETOU8_RETURN_CARRY
	OR A
	JR Z, FPSINGLETOU8_RETURN_CARRY
	INC A
	JR Z, FPSINGLETOU8_INFNAN

	ADD A, 256-135
	JR C, FPSINGLETOU8_RETURN_CARRY
	ADD A, 7
	JR NC, FPSINGLETOU8_RETURN_CARRY

	LD B, A
	INC B
	XOR A
	RL C
	RLA
	DJNZ $-3

	DEFB $FE
FPSINGLETOU8_RETURN_CARRY:
	SBC A, A
FPSINGLETOU8_RETURN:
	POP BC
	POP DE
	POP HL
	RET

FPSINGLETOU8_INFNAN:
	LD A, C
	ADD A, A
	OR D
	OR E
	SUB 1
	JR FPSINGLETOU8_RETURN_CARRY
	