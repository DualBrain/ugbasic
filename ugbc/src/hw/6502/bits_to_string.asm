
B2STRING:
    LDA #0
    STA $22
    STA $21
    STA $20
    STA $19
    JSR B2STRINGH2D
    LDX #9;
    LDY #0;
B2STRINGL1:
    LDA RESBUFFER,x
    BNE B2STRINGL2
    DEX
    BNE B2STRINGL1
B2STRINGL2:
    LDA RESBUFFER,X
    ORA #$30
    STA ($23),Y
    INY
    DEX
    BPL B2STRINGL2
    JMP B2STRINGEND
B2STRINGH2D:
    LDX #0
    B2STRINGL3:
    JSR B2STRINGDIV10
    STA RESBUFFER, X
    INX
    CPX #10
    BNE B2STRINGL3
    RTS
B2STRINGDIV10:
    LDY #32
    LDA #0
    CLC
B2STRINGL4:
    ROL
    CMP #10
    BCC B2STRINGSSKIP
    SBC #10
B2STRINGSSKIP:
    ROL $19
    ROL $20
    ROL $21
    ROL $22
    DEY
    BPL B2STRINGL4
    RTS
B2STRINGEND:
    TYA
    STA $25
    RTS

RESBUFFER:  .RES    10