/**
 * @brief Emit code for <strong>IMAGE LOAD(...)</strong>
 * 
 * @param _environment Current calling environment
 * @param _filename Filename to read into buffer
 * @param _mode Mode to use to convert data
 */
/* <usermanual>
@keyword MOB HIDE

@english
The ''MOB HIDE'' command allows you to show (enable) a movable object.

@italian
Il comando ''MOB HIDE'' permette di mostrare (abilitare) un oggetto mobile.

@syntax MOB HIDE [index]

@example MOB HIDE starship

@usedInExample mobs_example_01.bas

@target all
</usermanual> */
void mob_hide( Environment * _environment, char * _index ) {

    Variable * index = variable_retrieve_or_define( _environment, _index, VT_BYTE, 0 );

    cpu_mobhide( _environment, index->realName );

}
